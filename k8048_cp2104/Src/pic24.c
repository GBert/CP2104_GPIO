{
	uint32_t reg  = 0x800000 | (regin << 3);
	uint32_t visi = 0x880000 | (pic24_conf.visi << 3);
	uint16_t regout;

	pic24_six(k, reg,  1);	/* MOV #reg, W0 */
	pic24_six(k, visi, 1);	/* MOV W0, VISI */

	regout = pic24_regout(k);

	return regout;
}

/*
 * INITIALISE TBLPAG, THE READ POINTER (W6) AND THE WRITE POINTER (W7) FOR TBLRD INSTRUCTION
 *
 *  AFFECTS
 *   TBLPAG = ADDRESS HIGH
 *   W6 = ADDRESS LOW
 *   W7 = VISI
 */
void
pic24_set_read_pointer(struct k8048 *k, uint32_t addr)
{
	uint32_t high   = 0x200000 | ((addr & 0x00FF0000) >> 12);
	uint32_t tblpag = 0x880000 | (pic24_conf.tblpag   << 3);
	uint32_t low    = 0x200006 | ((addr & 0x0000FFFF) << 4);
	uint32_t visi   = 0x200007 | (pic24_conf.visi     << 4);
#ifdef DEBUG
	printf("%s: PCL=%04X\n", __func__, pic24_readreg(k, _PCL));
#endif
	pic24_six(k, high,   0);	/* MOV #<SourceAddress23:16>, W0 */
	pic24_six(k, tblpag, 0);	/* MOV W0, TPLPAG                */
	pic24_six(k, low,    0);	/* MOV #<SourceAddress15:0>, W6  */
	pic24_six(k, visi,   1);	/* MOV #VISI, W7                 */
}

/*
 * INITIALISE TBLPAG AND THE WRITE POINTER (W7) FOR TBLWT INSTRUCTION
 *
 *  AFFECTS
 *   TBLPAG = ADDRESS HIGH
 *   W7 = ADDRESS LOW
 */
void
pic24_set_write_pointer(struct k8048 *k, uint32_t addr)
{
	uint32_t high   = 0x200000 | ((addr & 0x00FF0000) >> 12);
	uint32_t tblpag = 0x880000 | (pic24_conf.tblpag   << 3);
	uint32_t low    = 0x200007 | ((addr & 0x0000FFFF) << 4);
#ifdef DEBUG
	printf("%s: PCL=%04X\n", __func__, pic24_readreg(k, _PCL));
#endif
	pic24_six(k, high,   0);	/* MOV #<DestinationAddress23:16>, W0 */
	pic24_six(k, tblpag, 0);	/* MOV W0, TPLPAG                */
	pic24_six(k, low,    1);	/* MOV #<DestinationAddress15:0>, W7  */
}

/*
 * TABLE READ 16-BIT WITH POST INCREMENT
 *
 *  REQUIRES
 *   TBLPAG = ADDRESS HIGH
 *   W6 = ADDRESS LOW
 *   W7 = VISI
 *
 *   96 0B BA TBLRDL [W6], [W7]
 *   B6 0B BA TBLRDL [W6++], [W7]
 */
static inline uint16_t
pic24_table_read16_post_increment(struct k8048 *k)
{
	uint16_t word;

	/* TABLE READ LOW (WORD) [TBLPAG]:[W6] => [W7] (VISI), INCREMENT W6                    */
	pic24_six(k, 0xBA0BB6, pic24_conf.tblnop);	/* TBLRDL [W6++], [W7]                 */
	word = pic24_regout(k);				/* Clock out contents of VISI register */

	return word;
}

/*
 * TABLE READ 24-BIT WITH POST INCREMENT
 *
 *  REQUIRES
 *   TBLPAG = ADDRESS HIGH
 *   W6 = ADDRESS LOW
 *   W7 = VISI
 *
 *   96 0B BA TBLRDL [W6], [W7]
 *   B6 0B BA TBLRDL [W6++], [W7]
 *
 *   96 8B BA TBLRDH [W6], [W7]
 *   B6 8B BA TBLRDH [W6++], [W7]
 */
uint32_t
pic24_table_read24_post_increment(struct k8048 *k)
{
	uint32_t word;
	uint16_t low, high;

	/* TABLE READ LOW  WORD [TBLPAG]:[W6] => [W7] (VISI)                                   */
	pic24_six(k, 0xBA0B96, pic24_conf.tblnop);	/* TBLRDL [W6], [W7]		       */
	low = pic24_regout(k);				/* Clock out contents of VISI register */

	/* TABLE READ HIGH WORD [TBLPAG]:[W6++] => [W7] (VISI)                                 */
	pic24_six(k, 0xBA8BB6, pic24_conf.tblnop);	/* TBLRDH [W6++], [W7]                 */
	high = pic24_regout(k);				/* Clock out contents of VISI register */

	word = ((high << 16) | low) & PIC24_MASK;

	return word;
}

/*
 * TABLE READ 2 x 24-BIT WITH POST INCREMENT
 *
 *  REQUIRES
 *   TBLPAG = ADDRESS HIGH
 *   W6 = ADDRESS LOW
 *   W7 = VISI
 */
void
pic24_table_read48_post_increment(struct k8048 *k, uint32_t *word0, uint32_t *word1)
{
	uint16_t msb;

	pic24_six(k, 0xBA0B96, pic24_conf.tblnop);	/* TBLRDL [W6], [W7]		       */
	*word0 = pic24_regout(k);			/* Clock out contents of VISI register */

	pic24_six(k, 0xBADBB6, pic24_conf.tblnop);	/* TBLRDH.B [W6++], [W7++]             */
	pic24_six(k, 0xBAD3D6, pic24_conf.tblnop);	/* TBLRDH.B [++W6], [W7--]             */
	msb = pic24_regout(k);				/* Clock out contents of VISI register */

	pic24_six(k, 0xBA0BB6, pic24_conf.tblnop);	/* TBLRDL [W6++], [W7]		       */
	*word1 = pic24_regout(k);			/* Clock out contents of VISI register */

	*word0 |= (msb & 0x00FF) << 16;
	*word1 |= (msb & 0xFF00) << 8;
}

/*
 * WAIT FOR PROGRAM/ERASE COMPLETION
 */
void
pic24_nvmcon_write_completion(struct k8048 *k, uint16_t mask, uint16_t word)
{
	struct timeval tv1 = {0}, tv2 = {0}, tv3 = {0};
	uint16_t regout;

	gettimeofday(&tv1, NULL);
	io_program_out(k, 0, 28); /* NOP */
	do {
		gettimeofday(&tv2, NULL);
		timersub(&tv2, &tv1, &tv3);

		regout = pic24_readreg(k, pic24_conf.nvmcon) & mask;
		pic24_goto200(k); /* GOTO 200 */
	}
	while ((regout != word) && (tv3.tv_sec < PIC24_TIMEOUT));

	if (tv3.tv_sec >= PIC24_TIMEOUT)
		printf("%s: information: program/erase timed out.\n", __func__);
}

/*
 * INITIATE PROGRAM/ERASE THEN DELAY FOR COMPLETION
 *
 * DS70102H-page 70 dsPIC30F4013
 * DS70284C-page 30 dsPIC30F1010
 */
void
pic24_nvmcon_write_external(struct k8048 *k, uint32_t tdly)
{
 	/* NVMCON = 0x760 */
	pic24_six(k, 0x200558, 0);	/* MOV #0x55, W8      */
	pic24_six(k, 0x200AA9, 0);	/* MOV #0xAA, W9      */
	pic24_six(k, 0x883B38, 0);	/* MOV W8, NVMKEY     */
	pic24_six(k, 0x883B39, 0);	/* MOV W9, NVMKEY     */
	pic24_six(k, 0xA8E761, 4);	/* BSET NVMCON, #WR   */
	io_usleep(k, tdly);
	pic24_six(k, 0xA9E761, 4);	/* BCLR NVMCON, #WR   */
	pic24_goto200(k);
}

/*
 * INITIATE PROGRAM/ERASE THEN WAIT FOR COMPLETION
 *
 * DS39768D-page 17 PIC24FJ64GA002
 */
static inline void
pic24_nvmcon_write_internal(struct k8048 *k)
{
 	/* NVMCON = 0x760 */
	pic24_six(k, 0xA8E761, 2); /* BSET NVMCON, #WR */
	pic24_nvmcon_write_completion(k, 0x8000, 0x0000);
}

/*
 * RESTORE CALIBRATION WORDS AFTER EXEC ERASED
 */
void
pic24_restore_calibration(struct k8048 *k)
{
	if (pic24_map[pic24_index].calibaddr == 0)
		return; /* Not supported */
	
	pic24_write_buffer_init(k);

	pic24_write_buffered(k, PIC24_REGIONEXEC, pic24_map[pic24_index].codepanelsize, PIC24_PANEL_BEGIN);
	uint32_t address = pic24_map[pic24_index].calibaddr;
	for (uint32_t i = 0; i < pic24_map[pic24_index].ncalib; ++i) {
		pic24_write_buffered(k, address, pic24_conf.calib[i], PIC24_PANEL_UPDATE);
		address += 2;
	}
	pic24_write_buffered(k, PIC_VOID, PIC_VOID, PIC24_PANEL_END);
}

/*
 * BLANK dsPIC30
 *
 * DS70102H-page 69 dsPIC30F4013
 */
void
pic24_dsPIC30F_erase(struct k8048 *k, uint32_t tdly)
{
 	/* NVMCON = 0x760 */
	pic24_program_verify(k);

	/* ERASE CONFIG */
	if (pic24_map[pic24_index].deviceid == dsPIC30F5011 ||
		pic24_map[pic24_index].deviceid == dsPIC30F5013) {
		pic24_conf.config[3] = PIC24_CONFIG_PENDING; /* FBS = 0x0000 */
		pic24_conf.config[4] = PIC24_CONFIG_PENDING; /* FSS = 0x0000 */
	}
	pic24_write_config_words(k);

	/* ERASE UNIT */
	pic24_write_program(k, pic24_map[pic24_index].fuidaddr, pic24_conf.fuid,
		pic24_map[pic24_index].nfuid);

	/* 407F ERASE CODE, DATA, EXEC & PROTECTION */
	pic24_six(k, 0x2407FA, 0);	/* MOV #0x407F, W10   */
	pic24_six(k, 0x883B0A, 0);	/* MOV W10, NVMCON    */

	/* EXTERNALLY TIME */
	pic24_nvmcon_write_external(k, tdly);

	pic24_standby(k);
}

