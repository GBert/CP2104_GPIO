/*
 * GET CONFIGURATION
 */
void
pic24_read_config_memory(struct k8048 *k, int flag __attribute__((unused)))
{
	uint32_t dev;

	/* NULL device */
	pic24_index = PIC24_SIZE;

	/* Reset configuraton */
	memset(&pic24_conf, 0, sizeof(pic24_conf));

	/* Device selected by user */
	if (k->devicename[0]) {
		for (dev = 0; pic24_map[dev].deviceid; ++dev) {
			if (strcasecmp(pic24_map[dev].devicename, k->devicename) == 0) {
				/* Device recognised */
				pic24_index = dev;
				break;
			}
		}
		if (pic24_map[dev].deviceid == 0) {
			printf("%s: fatal error: unknown device: [%s]\n", __func__, k->devicename);
			io_exit(k, EX_SOFTWARE); /* Panic */
		}
	}

	/* Device configuration */
	switch (pic24_map[pic24_index].datasheet) {
	default:pic24_conf.tblpag  = 0x0032;
		pic24_conf.nvmcon  = 0x0760;
		pic24_conf.visi    = 0x0784;
		pic24_conf.tblnop  = 2 + 1 - 1;	/* TBLRD: 2 CYCLES + 1 STALL */
		pic24_conf.gotonop = 2 + 0 - 1;	/* GOTO:  2 CYCLES + 0 STALL */
		break;
	case DS70663D:
		pic24_conf.tblpag  = 0x0054;
		pic24_conf.nvmcon  = 0x0728;
		pic24_conf.visi    = 0x0F88;
		pic24_conf.tblnop  = 5 + 1 - 1;	/* TBLRD: 5 CYCLES + 1 STALL */
		pic24_conf.gotonop = 4 + 0 - 1;	/* GOTO:  4 CYCLES + 0 STALL */
		break;
	}
	
	pic24_program_verify(k);

	/* Device id/rev */
	pic24_set_read_pointer(k, PIC24_DEVID);
	pic24_conf.deviceid = pic24_table_read16_post_increment(k);
	pic24_conf.revision = pic24_table_read16_post_increment(k);

	dev = 0;
	while (pic24_map[dev].deviceid) {
		if (pic24_map[dev].deviceid == pic24_conf.deviceid)
			break;
		++dev;
	}
	if (!pic24_map[dev].deviceid) {
		if (pic24_conf.deviceid == 0x0000 || pic24_conf.deviceid == 0xFFFF) {
			printf("%s: fatal error: %s.\n",
				__func__, io_fault(k, pic24_conf.deviceid));
		} else {
			printf("%s: fatal error: device unknown: [%06X] [%06X]\n",
				__func__, pic24_conf.deviceid, pic24_conf.revision);
		}
		pic24_standby(k);
		io_exit(k, EX_SOFTWARE); /* Panic */
	}

	/* Device recognised */
	pic24_index = dev;

	/* App-id */
	if ((pic24_map[pic24_index].datasheet == DS70102H) ||	/* dsPIC30F      */
		(pic24_map[pic24_index].datasheet == DS70284B))	/* dsPIC30F SMPS */
		pic24_set_read_pointer(k, dsPIC30F_APPID_ADDR);
	else if (pic24_map[pic24_index].datasheet == DS70663D)
		pic24_set_read_pointer(k, dsPIC33E_APPID_ADDR);
	else
		pic24_set_read_pointer(k, PIC24_APPID_ADDR);
	pic24_conf.appid = pic24_table_read24_post_increment(k);

	/* Config */
	pic24_set_read_pointer(k, pic24_map[pic24_index].configaddr);
	for (uint32_t i = 0; i < pic24_map[pic24_index].nconfig; ++i)
		pic24_conf.config[i] = pic24_table_read16_post_increment(k);

	/* Fuid */
	if (pic24_map[pic24_index].fuidaddr) {
		pic24_set_read_pointer(k, pic24_map[pic24_index].fuidaddr);
		for (uint32_t i = 0; i < pic24_map[pic24_index].nfuid; ++i)
			pic24_conf.fuid[i] = pic24_table_read24_post_increment(k);
	}

	/* Calibration */
	if (pic24_map[pic24_index].calibaddr) {
		pic24_set_read_pointer(k, pic24_map[pic24_index].calibaddr);
		for (uint32_t i = 0; i < pic24_map[pic24_index].ncalib; ++i)
			pic24_conf.calib[i] = pic24_table_read24_post_increment(k);
	}

	pic24_standby(k);
}

/*
 * GET PROGRAM FLASH SIZE
 *
 *  RETURN SIZE IN WORDS
 */
uint32_t
pic24_get_program_size(uint32_t *addr)
{
	*addr = 0;

	return pic24_map[pic24_index].flash;
}

/*
 * GET DATA EEPROM/FLASH SIZE
 *
 *  RETURN SIZE IN BYTES
 */
uint32_t
pic24_get_data_size(uint32_t *addr)
{
	if (pic24_map[pic24_index].eeprom) {
		*addr = PIC24_EXEC_LOW - pic24_map[pic24_index].eeprom;
	} else {
		*addr = 0;
	}
	return pic24_map[pic24_index].eeprom;
}

/*
 * GET EXECUTIVE SIZE
 *
 *  RETURN SIZE IN WORDS
 */
uint32_t
pic24_get_executive_size(uint32_t *addr)
{
	uint32_t size = 0;

	*addr = PIC24_EXEC_LOW;

	if ((pic24_map[pic24_index].datasheet == DS70102H) ||	/* dsPIC30F      */
		(pic24_map[pic24_index].datasheet == DS70284B))	/* dsPIC30F SMPS */
		size = dsPIC30F_EXEC_HIGH - PIC24_EXEC_LOW;
	else if (pic24_map[pic24_index].datasheet == DS70663D)	/* dsPIC33E */
		size = dsPIC33E_EXEC_HIGH - PIC24_EXEC_LOW;
	else
		size = PIC24_EXEC_HIGH - PIC24_EXEC_LOW;

	return (size >> 1);
}

/*
 * READ PROGRAM FLASH MEMORY BLOCK ADDR .. ADDR + SIZE
 *
 *  RETURN ADDR
 */
#define GOTO200 (32)
uint32_t
pic24_read_program_memory_block(struct k8048 *k, uint32_t *data, uint32_t addr, uint32_t size)
{
	if (size & 1) {
		printf("%s: fatal error: block size (%u) uneven\n", __func__, size);
		io_exit(k, EX_SOFTWARE); /* Panic */
	}

	pic24_program_verify(k);

	pic24_set_read_pointer(k, addr);
	uint32_t pc = 0;
	for (uint32_t i = 0; i < size;) {
		if ((pc++ % GOTO200) == 0)
			pic24_goto200(k);
		uint32_t word0, word1;
		pic24_table_read48_post_increment(k, &word0, &word1);
		data[i++] = (uint32_t)word0;
		data[i++] = (uint32_t)word1;
#ifdef DEBUG
		char d[STRLEN];
		snprintf(d, STRLEN, "%s() addr=%06X size=%d", __func__, addr, size);
		printf("%s: PCL=%04X\n", d, pic24_readreg(k, _PCL));
#endif
		addr += 4;
		if ((addr % 0x0010000) == 0)
			pic24_set_read_pointer(k, addr);
	}

	pic24_standby(k);

	return addr;
}

/*
 * READ DATA EEPROM/FLASH MEMORY BLOCK ADDR .. ADDR + SIZE
 *
 *  RETURN ADDR
 */
uint32_t
pic24_read_data_memory_block(struct k8048 *k, uint16_t *data, uint32_t addr, uint16_t size)
{
	pic24_program_verify(k);

	pic24_set_read_pointer(k, addr);
	uint32_t pc = 0;
	for (uint32_t i = 0; i < size;) {
		if ((pc++ % GOTO200) == 0)
			pic24_goto200(k);
		data[i++] = pic24_table_read16_post_increment(k);
#ifdef DEBUG
		char d[STRLEN];
		snprintf(d, STRLEN, "%s() addr=%06X size=%d", __func__, addr, size);
		printf("%s: PCL=%04X\n", d, pic24_readreg(k, _PCL));
#endif
	}

	pic24_standby(k);

	return addr;
}

