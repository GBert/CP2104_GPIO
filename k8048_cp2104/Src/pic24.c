/*
 * 0000
 *
 * SIX (SHIFT IN 24-BIT INSTRUCTION AND EXECUTE)
 */
static inline void
pic24_six(struct k8048 *k, uint32_t data, uint8_t nops)
{
	io_program_out(k, 0x0, 4);		/* SIX        */
	io_program_out(k, data, 24);
	if (nops)
		io_program_out(k, 0, 28 * nops);/* NOP [,NOP] */
}

/*
 * 0001
 *
 * REGOUT (SHIFT OUT THE VISI REGISTER)
 */
static inline uint16_t
pic24_regout(struct k8048 *k)
{
	uint16_t word;

	io_program_out(k, 0x1, 4);		/* REGOUT */
	word = io_program_in(k, 24) >> 8;	/* VISI   */
	io_program_out(k, 0, 28);		/* NOP    */

	return word;
}

