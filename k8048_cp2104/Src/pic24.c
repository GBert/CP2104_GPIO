/*
 * WRITE 24-BIT PROGRAM BUFFER TO 24-BIT PROGRAM FLASH
 */
void
pic24_write_program(struct k8048 *k, uint32_t address, uint32_t *buffer, uint32_t buffer_size)
{
#if 0
	printf("%s() address=%06X buffer_size=%d\n", __func__, address, buffer_size);
	pic24_dumphexcode(k, address, buffer_size, buffer);
#else
	switch (pic24_map[pic24_index].datasheet) {
	case DS70102H: /* dsPIC30F      */
	case DS70284B: /* dsPIC30F SMPS */
 		/* NVMCON = 0x760 */
		pic24_six(k, 0x24001A, 0); 	/* MOV #0x4001, W10  */
		pic24_six(k, 0x883B0A, 0);	/* MOV W10, NVMCON   */
	default:break;
	}
	pic24_set_write_pointer(k, address);
	for (uint32_t i = 0; i < buffer_size; i += 4) {
		uint16_t lsw0 = buffer[i + 0];
		uint16_t lsw1 = buffer[i + 1];
		uint16_t lsw2 = buffer[i + 2];
		uint16_t lsw3 = buffer[i + 3];
		uint8_t  msb0 = buffer[i + 0] >> 16;
		uint8_t  msb1 = buffer[i + 1] >> 16;
		uint8_t  msb2 = buffer[i + 2] >> 16;
		uint8_t  msb3 = buffer[i + 3] >> 16;

		uint32_t w0 = 0x200000 | (lsw0 << 4);
		uint32_t w1 = 0x200001 | (msb1 << 12) | (msb0 << 4);
		uint32_t w2 = 0x200002 | (lsw1 << 4);
		uint32_t w3 = 0x200003 | (lsw2 << 4);
		uint32_t w4 = 0x200004 | (msb3 << 12) | (msb2 << 4);
		uint32_t w5 = 0x200005 | (lsw3 << 4);

		/* LOAD W0:W5 WITH 4 INSTRUCTIONS */
		pic24_six(k, w0, 0);	/* MOV #<LSW0>, W0      */
		pic24_six(k, w1, 0);	/* MOV #<MSB1:MSB0>, W1 */
		pic24_six(k, w2, 0);	/* MOV #<LSW1>, W2      */
		pic24_six(k, w3, 0); 	/* MOV #<LSW2>, W3      */
		pic24_six(k, w4, 0); 	/* MOV #<MSB3:MSB2>, W4 */
		pic24_six(k, w5, 0);	/* MOV #<LSW3>, W5      */

		/* SET THE READ POINTER AND LOAD LATCHES */
		pic24_six(k, 0xEB0300, 1);/* CLR W6                  */
		pic24_six(k, 0xBB0BB6, 2);/* TBLWTL   [W6++], [W7]   */
		pic24_six(k, 0xBBDBB6, 2);/* TBLWTH.B [W6++], [W7++] */
		pic24_six(k, 0xBBEBB6, 2);/* TBLWTH.B [W6++], [++W7] */
		pic24_six(k, 0xBB1BB6, 2);/* TBLWTL   [W6++], [W7++] */
		pic24_six(k, 0xBB0BB6, 2);/* TBLWTL   [W6++], [W7]   */
		pic24_six(k, 0xBBDBB6, 2);/* TBLWTH.B [W6++], [W7++] */
		pic24_six(k, 0xBBEBB6, 2);/* TBLWTH.B [W6++], [++W7] */
		pic24_six(k, 0xBB1BB6, 2);/* TBLWTL   [W6++], [W7++] */
	}	

	switch (pic24_map[pic24_index].datasheet) {
	case DS70102H: /* dsPIC30F      */
	case DS70284B: /* dsPIC30F SMPS */
		/* DS70102H-page 90 dsPIC30F4013 TPROG P12(2ms MAX) */
		/* DS70284B-page 44 dsPIC30F1010 TPROG P18(4ms MAX) */
		pic24_nvmcon_write_external(k, 4000);
		break;
	default:pic24_nvmcon_write_internal(k);
		break;
	}
#endif
}

/*
 * WRITE 16-BIT DATA BUFFER TO 16-BIT DATA EEPROM
 *
 *  TBLPAG = 0x7F
 */
void
pic24_write_data(struct k8048 *k, uint32_t address, uint32_t *buffer, uint32_t buffer_size)
{
	switch (pic24_map[pic24_index].datasheet) {
	case DS70102H: /* dsPIC30F */
 		/* NVMCON = 0x760 */
		pic24_six(k, 0x24005A, 0); 	/* MOV #0x4005, W10  */
		pic24_six(k, 0x883B0A, 0);	/* MOV W10, NVMCON   */
	default:break;
	}
	pic24_set_write_pointer(k, address);
	for (uint32_t i = 0; i < buffer_size; i += 4) {
		uint32_t w0 = 0x200000 | ((0xFFFF & buffer[i + 0]) << 4);
		uint32_t w1 = 0x200001 | ((0xFFFF & buffer[i + 1]) << 4);
		uint32_t w2 = 0x200002 | ((0xFFFF & buffer[i + 2]) << 4);
		uint32_t w3 = 0x200003 | ((0xFFFF & buffer[i + 3]) << 4);

		/* LOAD W0:W3 WITH 4 DATA WORDS */
		pic24_six(k, w0, 0);	/* MOV #<WORD0>, W0 */
		pic24_six(k, w1, 0);	/* MOV #<WORD1>, W1 */
		pic24_six(k, w2, 0);	/* MOV #<WORD2>, W2 */
		pic24_six(k, w3, 0); 	/* MOV #<WORD3>, W3 */

		/* SET THE READ POINTER AND LOAD LATCHES */
		pic24_six(k, 0xEB0300, 1);/* CLR W6                  */
		pic24_six(k, 0xBB1BB6, 2);/* TBLWTL   [W6++], [W7++] */
		pic24_six(k, 0xBB1BB6, 2);/* TBLWTL   [W6++], [W7++] */
		pic24_six(k, 0xBB1BB6, 2);/* TBLWTL   [W6++], [W7++] */
		pic24_six(k, 0xBB1BB6, 2);/* TBLWTL   [W6++], [W7++] */
	}	

	switch (pic24_map[pic24_index].datasheet) {
	case DS70102H: /* dsPIC30F */
		/* DS70102H-page 90 dsPIC30F4013 TPROG P12(2ms MAX) */
		pic24_nvmcon_write_external(k, 2000);
		break;
	default:pic24_nvmcon_write_internal(k);
		break;
	}
}

/*
 * WRITE 24-BIT PROGRAM WORD TO 24-BIT PROGRAM FLASH
 *
 *  TBLPAG = 0x00
 */
void
pic24_write_program_word24(struct k8048 *k, uint32_t address, uint32_t word)
{
	uint32_t w2 = 0x200002 | ((0x0000FFFF & address) << 4);
	uint32_t w5 = 0x200005 | ((0x0000FFFF & word)    << 4);
	uint32_t w6 = 0x200006 | ((0x00FF0000 & word)    >> 12);

	pic24_six(k, w2, 0);		/* MOV #<DestinationAddress15:0>, W2 */
	pic24_six(k, w5, 0);		/* MOV #<LOW_WORD>, W5               */
	pic24_six(k, w6, 1);		/* MOV #<HIGH_BYTE>, W6              */
	pic24_six(k, 0xBB0905, 2);	/* TBLWTL W5, [W2]                   */
	pic24_six(k, 0xBB9906, 3);	/* TBLWTH.B W6, [W2++]               */
	pic24_nvmcon_write_internal(k);
}

/*
 * WRITE 24-BIT x 2 PROGRAM WORDS TO 24-BIT PROGRAM FLASH
 *
 *  TBLPAG = 0xFA
 */
void
pic24_write_program_word48(struct k8048 *k, uint32_t address, uint32_t word0, uint32_t word1)
{
 	/* NVMCON = 0x728 */
	uint16_t lsw0 = word0;
	uint16_t lsw1 = word1;
	uint8_t  msb0 = word0 >> 16;
	uint8_t  msb1 = word1 >> 16;

	uint32_t w0 = 0x200000 | (lsw0 << 4);
	uint32_t w1 = 0x200001 | (msb1 << 12) | (msb0 << 4);
	uint32_t w2 = 0x200002 | (lsw1 << 4);

	/* LOAD W0:W2 WITH 2 INSTRUCTIONS */
	pic24_six(k, w0, 0);	/* MOV #<LSW0>, W0      */
	pic24_six(k, w1, 0);	/* MOV #<MSB1:MSB0>, W1 */
	pic24_six(k, w2, 0);	/* MOV #<LSW1>, W2      */

	/* SET THE READ POINTER (W6) AND WRITE POINTER (W7) AND LOAD THE WRITE LATCHES */
	pic24_six(k, 0xEB0300, 1);/* CLR W6                  */
	pic24_six(k, 0xEB0380, 1);/* CLR W7                  */
	pic24_six(k, 0xBB0BB6, 2);/* TBLWTL   [W6++], [W7]   */
	pic24_six(k, 0xBBDBB6, 2);/* TBLWTH.B [W6++], [W7++] */
	pic24_six(k, 0xBBEBB6, 2);/* TBLWTH.B [W6++], [++W7] */
	pic24_six(k, 0xBB1BB6, 2);/* TBLWTL   [W6++], [W7++] */

	uint32_t w3 = 0x200003 | ((address & 0x00FFFF) << 4);
	uint32_t w4 = 0x200004 | ((address & 0xFF0000) >> 12);

	/* SET THE NVMADRU/NVMADR REGISTER-PAIR TO POINT TO THE CORRECT ADDRESS */
	pic24_six(k, w3, 0);      /* MOV #DestinationAddress<15:0>, W3  */
	pic24_six(k, w4, 0);	  /* MOV #DestinationAddress<23:16>, W4 */
	pic24_six(k, 0x883953, 0);/* MOV W3, NVMADR  */
	pic24_six(k, 0x883964, 0);/* MOV W4, NVMADRU */

	/* SET THE NVMCON TO PROGRAM TWO INSTRUCTION WORDS */
	pic24_six(k, 0x24001A, 1);/* MOV 0x4001, W10 */
	pic24_six(k, 0x88394A, 2);/* MOV W10, NVMCON */

	/* INITIATE THE WRITE CYCLE */
	pic24_six(k, 0x200551, 0);/* MOV #0x55, W1     */
	pic24_six(k, 0x883971, 0);/* MOV W1, NVMKEY    */
	pic24_six(k, 0x200AA1, 0);/* MOV #0xAA, W1     */
	pic24_six(k, 0x883971, 0);/* MOV W1, NVMKEY    */
	pic24_six(k, 0xA8E729, 3);/* BSET NVMCON, #WR  */

	/* DS700000657H TWW(53.8us) or TWW(54.4us)     */
	io_usleep(k, 55);	  /* P13=TWW P10=400ns */

	pic24_nvmcon_write_completion(k, 0xFFFF, 0x4001);
}

/*
 * WRITE 16-BIT DATA WORD TO 16-BIT DATA EEPROM
 *
 *  TBLPAG = 0x7F
 */
void
pic24_write_data_word16(struct k8048 *k, uint32_t address, uint32_t word)
{
	uint32_t w2 = 0x200002 | ((0x0000FFFF & address) << 4);
	uint32_t w5 = 0x200005 | ((0x0000FFFF & word)    << 4);

	pic24_six(k, w2, 0);		/* MOV #<DestinationAddress15:0>, W2 */
	pic24_six(k, w5, 0);		/* MOV #<DATA_WORD_VALUE>, W5        */
	pic24_six(k, 0xBB0905, 2);	/* TBLWTL W5, [W2]                   */

	pic24_nvmcon_write_internal(k);
}

/*
 * WRITE BUFFER TO 24-BIT PROGRAM FLASH OR 16-BIT DATA EEPROM
 */
void
pic24_write_buffer(struct k8048 *k, uint32_t region, uint32_t address, uint32_t *buffer, uint32_t buffer_size)
{
	switch (region) {
	case PIC24_REGIONCODE:
	case PIC24_REGIONEXEC:
		if (buffer_size == 1) {		/* dsPIC33FJ/PIC24FJXXMC WORD WRITING */
			pic24_write_program_word24(k, address, buffer[0]);
		} else if (buffer_size == 2) {	/* dsPIC33E/PIC24E DWORD WRITING */
			pic24_write_program_word48(k, address, buffer[0], buffer[1]);
		} else {			/* PANEL WRITING */
			pic24_write_program(k, address, buffer, buffer_size);
		}
		break;
	case PIC24_REGIONDATA:
		if (buffer_size == 1) {		/* PIC24F KA KM/KL WORD WRITING */
			pic24_write_data_word16(k, address, buffer[0]);
		} else {			/* PANEL WRITING */
			pic24_write_data(k, address, buffer, buffer_size);
		}
		break;
	default:printf("%s: warning: region unsupported [%d]\n",
		__func__, region);
		break;
	}
}

/*
 * WRITE BUFFERED
 */
void
pic24_write_buffered(struct k8048 *k, uint32_t data1, uint32_t data2, int mode)
{
	static uint32_t buffer_region = PIC24_REGIONNOTSUP;
	static uint32_t buffer_address = 0;
	static uint32_t *buffer = NULL;
	static uint32_t buffer_size = 0;
	static uint32_t write_pending = 0;

	if (mode == PIC24_PANEL_BEGIN || mode == PIC24_PANEL_END) {
		if (buffer) {
			if (write_pending) {
				write_pending = 0;
				pic24_write_buffer(k, buffer_region, buffer_address << 1, buffer, buffer_size);
			}
			free(buffer);
			buffer_region = PIC24_REGIONNOTSUP;
			buffer_address = 0;
			buffer = NULL;
			buffer_size = 0;
		}
	}
	if (mode == PIC24_PANEL_BEGIN) {
		buffer_region = data1;
		buffer_size = data2;
	}
	if (mode == PIC24_PANEL_BEGIN || mode == PIC24_PANEL_UPDATE) {
		if (buffer == NULL) {
			if (buffer_size == 0) {
				printf("%s: fatal error: zero sized buffer\n", __func__);
				io_exit(k, EX_SOFTWARE); /* Panic */
			}
			buffer = malloc(sizeof(uint32_t) * buffer_size);
			if (buffer == NULL) {
				printf("%s: fatal error: malloc failed\n", __func__);
				io_exit(k, EX_SOFTWARE); /* Panic */
			}
			memset((void *)buffer, -1, sizeof(uint32_t) * buffer_size);
		}
	}
	if (mode == PIC24_PANEL_UPDATE) {
		uint32_t address, new_address, boundary, mask;

		boundary = 0 - buffer_size;
		mask = buffer_size - 1;
		address = data1 >> 1;
		new_address = address & boundary;
		if (new_address != buffer_address) {
			if (write_pending) {
				write_pending = 0;
				pic24_write_buffer(k, buffer_region, buffer_address << 1, buffer, buffer_size);
				memset((void *)buffer, -1, sizeof(uint32_t) * buffer_size);
			}
			buffer_address = new_address;
		}
		buffer[address & mask] = data2;
		write_pending++;
	}
}

/*
 * CONFIG INIT
 *
 *  SETUP FOR CONFIG OR FUID WRITING IN THE CONFIG SPACE
 */
void
pic24_write_config_init1(struct k8048 *k)
{
	switch (pic24_map[pic24_index].datasheet) {
	case DS39919C:
	case DS30625D:
		/* PIC24F KA      */
		/* PIC24F KM/KL   */
		/* NVMCON = 0x760 */
		pic24_six(k, 0x24004A, 0); 	/* MOV #0x4004, W10    */
		pic24_six(k, 0x883B0A, 0);	/* MOV W10, NVMCON     */
		break;
	case DS70152H:
		/* dsPIC33F/PIC24H */
		/* NVMCON = 0x760  */
		pic24_six(k, 0x24000A, 0); 	/* MOV #0x4000, W10    */
		pic24_six(k, 0x883B0A, 0);	/* MOV W10, NVMCON     */
	default:break;
	}
}
void
pic24_write_config_init2(struct k8048 *k)
{
	switch (pic24_map[pic24_index].datasheet) {
	case DS70102H:
	case DS70284B:
		/* dsPIC30F       */
		/* dsPIC30F SMPS  */
		/* NVMCON = 0x760 */
		pic24_six(k, 0x24008A, 0);	/* MOV #0x4008, W10    */
		pic24_six(k, 0x883B0A, 0);	/* MOV W10, NVMCON     */
	default:break;
	}
}

void
pic24_write_config_word(struct k8048 *k, uint32_t address, uint16_t word)
{
	uint32_t w6 = 0x200006 | (word << 4);

	/* INITIALISE TBLPAG AND THE WRITE POINTER (W7) FOR TBLWT INSTRUCTION */
	pic24_set_write_pointer(k, address);	/* TBLPAG=0xF8 W7=<15:0> */

	/* LOAD THE CONFIGURATION DATA REGISTER TO W6 */
	pic24_six(k, w6, 1);			/* MOV #<VALUE>, W6      */

	/* WRITE THE CONFIGURATION REGISTER DATA TO THE WRITE LATCH AND INC. WRITE POINTER */
	pic24_six(k, 0xBB1B86, 2);		/* TBLWTL W6, [W7++]     */

	switch (pic24_map[pic24_index].datasheet) {
	case DS70102H: /* dsPIC30F      */
	case DS70284B: /* dsPIC30F SMPS */
		/* DS70102H-page 90 dsPIC30F4013 TPROG P12(2ms MAX) */
		/* DS70284B-page 44 dsPIC30F1010 TPROG P18(4ms MAX) */
		/* UNLOCK NVMCON AND WRITE */
		pic24_nvmcon_write_external(k, 4000);
		break;
	default:/* WRITE */
		pic24_nvmcon_write_internal(k);
		break;
	}
}

/*
 * WRITE CONFIG WORDS IN THE CONFIG SPACE
 */
uint32_t
pic24_write_config_words(struct k8048 *k)
{
	if (pic24_map[pic24_index].configaddr != PIC24_CONFIG_ADDR)
		return 0; /* Not supported here */

	pic24_write_config_init1(k);
	uint32_t n = 0;
	for (uint32_t i = 0; i < pic24_map[pic24_index].nconfig; ++i) {
		if (pic24_conf.config[i] & PIC24_CONFIG_PENDING) {
			uint32_t address = pic24_map[pic24_index].configaddr + 2 * i;
			pic24_write_config_init2(k);
			pic24_write_config_word(k, address, pic24_conf.config[i]);
			n++;
		}
	}

	return n;
}

/*
 * WRITE FUID WORDS IN THE CONFIG SPACE
 */
uint32_t
pic24_write_fuid_words(struct k8048 *k)
{
	if (pic24_map[pic24_index].fuidaddr < PIC24_CONFIG_ADDR)
		return 0; /* Not supported here */

	pic24_write_config_init1(k);
	uint32_t n = 0;
	for (uint32_t i = 0; i < pic24_map[pic24_index].nfuid; ++i) {
		if (pic24_conf.fuid[i] & PIC24_CONFIG_PENDING) {
			uint32_t address = pic24_map[pic24_index].fuidaddr + 2 * i;
			pic24_write_config_init2(k);
			pic24_write_config_word(k, address, pic24_conf.fuid[i]);
			n++;
		}
	}

	return n;
}

/*
 * WRITE CONFIGURATION & FUID IN THE CONFIG SPACE
 */
uint32_t
pic24_write_config(struct k8048 *k)
{
	if ((pic24_map[pic24_index].configaddr >= PIC24_CONFIG_ADDR) ||
		(pic24_map[pic24_index].fuidaddr >= PIC24_CONFIG_ADDR)) {
		uint32_t n = 0;

		pic24_program_verify(k);

		if (pic24_map[pic24_index].fuidaddr)
			n += pic24_write_fuid_words(k);

		if (pic24_map[pic24_index].configaddr)
			n += pic24_write_config_words(k);

		pic24_standby(k);
	
		return n;
	}
	return 0;
}

