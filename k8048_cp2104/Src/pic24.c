/*
 * DUMP HEX FLASH WORDS
 */
void
pic24_dumphexcode(struct k8048 *k, uint32_t address, uint32_t size, uint32_t *data)
{
	uint32_t i, j, nlines = 0;

	for (i = 0; i < size; address += 16, i += 8) {
#ifndef DEBUG
		if (pic_mtcode(PIC24_MASK, 8, &data[i]))
			continue;
#endif
		printf("[%06X] ", address);
		for (j = 0; j < 8; ++j)
			printf("%06X ", data[i + j] & PIC24_MASK);
		for (j = 0; j < 8; ++j) {
			putchar(PIC_CHAR(0xFF & data[i + j]));
			putchar(PIC_CHAR(0xFF & (data[i + j] >> 8)));
			putchar(PIC_CHAR(0xFF & (data[i + j] >> 16)));
		}
		putchar('\n');
		nlines++;
	}
	if (!nlines)
		printf("%s: information: program flash empty\n", __func__);
}

/*
 * DUMP INHX32 FLASH WORDS
 */
void
pic24_dumpinhxcode(struct k8048 *k, uint32_t address, uint32_t size, uint32_t *data)
{
	uint32_t i, j;

	/* PIC24: Extended address */
	pic_dumpaddr(address << 1, 1);

	for (i = 0; i < size; address += 8, i += 4) {
		if (pic_mtcode(PIC24_MASK, 4, &data[i]))
			continue;
		/* PIC24: Extended address */
		pic_dumpaddr(address << 1, 0);

		uint8_t cc, hb, mb, lb;
		mb = address >> 7;
		lb = address << 1;
		printf(":%02X%02X%02X00", 16, mb, lb);
		cc = 16 + mb + lb + 0x00;
		for (j = 0; j < 4; ++j) {
			lb = data[i + j];
			mb = data[i + j] >> 8;
			hb = data[i + j] >> 16;
			printf("%02X%02X%02X00", lb, mb, hb);
			cc = cc + lb + mb + hb + 0x00;
		}
		printf("%02X\n", (0x0100 - cc) & 0xFF);
	}
}

/*
 * DUMP HEX DATA EEPROM WORDS
 */
void
pic24_dumphexdata(struct k8048 *k, uint32_t address, uint32_t size, uint16_t *data)
{
	uint32_t i, j, nlines = 0;

	for (i = 0; i < (size / 2); address += 16, i += 8) {
		if (pic_mtdata(0xFFFF, 8, &data[i]))
			continue;
		printf("[%06X] ", address);
		for (j = 0; j < 8; ++j)
			printf("%04X ", data[i + j]);
		for (j = 0; j < 8; ++j) {
			putchar(PIC_CHAR(0xFF & data[i + j]));
			putchar(PIC_CHAR(0xFF & (data[i + j] >> 8)));
		}
		putchar('\n');
		nlines++;
	}
	if (!nlines)
		printf("%s: information: data eeprom empty\n", __func__);
}

